// Vectors
fn vectors_example() {
    // Define a vector
    let mut numbers = vec![1, 2, 3];

    // Add elements to the vector
    numbers.push(4);

    // Access vector elements
    let first_number = numbers[0];

    // Print vector elements
    println!("First number: {}", first_number);
}

fn main() {
    vectors_example();
}
